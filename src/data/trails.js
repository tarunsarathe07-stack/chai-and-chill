export const getTrailById = id => TRAILS.find(trail => trail.id === id) || TRAILS[0]

export const getTrailStops = trail => trail.stopIds
  .map(id => SPOTS.find(spot => spot.id === id))
  .filter(Boolean)

export const buildTrailShareText = trail => {
  const stops = getTrailStops(trail)
  return [
    `${trail.name} on Chai & Chill`,
    trail.line,
    '',
    `Best time: ${trail.bestTime}`,
    `Budget: ${trail.budget}`,
    `Area: ${trail.area}`,
    '',
    ...stops.map((spot, index) => `${index + 1}. ${spot.name} - ${spot.area} (${spot.signatureOrder})`),
    '',
    `Bawa note: ${trail.note}`,
    '',
    window.location.origin,
  ].join('\n')
}
